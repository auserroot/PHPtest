<?php
    /* 
        php的变量
            + 在js里面我们定义变量使用var关键字
            + 在php里面，我们定义变量没有关键字
                --> 以$开头的就表示一个变量
            + 当我定义变量的时候
                + $num = 100;
                + 变量名就叫做 $num
                + $不是定义变量的关键字，是变量名的一部分
            + 当你要使用变量的时候
                + 就直接使用$num就可以了
    */

    // 1 定义变量
    // 定义一个叫做$num的变量，他的值是100
    $num = 100;

    // 2 使用变量
    // 把$num这个变量输出一下
    echo $num;
?>